package com.qpp.model;

/**
 * text model
 * @author Kevin Liu 2014/08/19
 *
 */
public class TextInfo {
	/**text info**/
	private String text;
	/**font type**/
	private String fontFamily;
	/**font size**/
	private String fontSize;
	/**font color**/
	private String color;
	/**text align**/
	private String textAlign;
	/**is italic or not**/
	private boolean isItalic;
	/**is bold or not**/
	private boolean isBold;
	/**text decoration**/
	private String textDecoration;
	/**text width**/
	private String width;
	/**text height**/
	private String height;
	
	/**
	 * @return text
	 */
	public String getText() {
		return text;
	}
	
	/**
	 * set the text to text
	 * @param text
	 */
	public void setText(String text) {
		this.text = text;
	}
	
	/**
	 * @return fontFamily
	 */
	public String getFontFamily() {
		return fontFamily;
	}
	
	/**
	 * set the fontFamily to fontFamily
	 * @param fontFamily
	 */
	public void setFontFamily(String fontFamily) {
		this.fontFamily = fontFamily;
	}
	
	/**
	 * @return fontSize
	 */
	public String getFontSize() {
		return fontSize;
	}
	
	/**
	 * set the fontSize to fontSize
	 * @param fontSize
	 */
	public void setFontSize(String fontSize) {
		this.fontSize = fontSize;
	}
	
	/**
	 * @return color
	 */
	public String getColor() {
		return color;
	}
	
	/**
	 * set the color to color
	 * @param color
	 */
	public void setColor(String color) {
		this.color = color;
	}
	
	/**
	 * @return textAlign
	 */
	public String getTextAlign() {
		return textAlign;
	}
	
	/**
	 * set the textAlign to textAlign
	 * @param textAlign
	 */
	public void setTextAlign(String textAlign) {
		this.textAlign = textAlign;
	}
	
	/**
	 * @return isItalic
	 */
	public boolean getIsItalic() {
		return isItalic;
	}
	
	/**
	 * set the isItalic to isItalic
	 * @param isItalic
	 */
	public void setIsItalic(boolean isItalic) {
		this.isItalic = isItalic;
	}
	
	/**
	 * @return isBold
	 */
	public boolean getIsBold() {
		return isBold;
	}
	
	/**
	 * set the isBold to isBold
	 * @param isBold
	 */
	public void setIsBold(boolean isBold) {
		this.isBold = isBold;
	}
	
	/**
	 * @return textDecoration
	 */
	public String getTextDecoration() {
		return textDecoration;
	}
	
	/**
	 * set the textDecoration to textDecoration
	 * @param textDecoration
	 */
	public void setTextDecoration(String textDecoration) {
		this.textDecoration = textDecoration;
	}
	
	/**
	 * @return width
	 */
	public String getWidth() {
		return width;
	}
	
	/**
	 * set the width to width
	 * @param width
	 */
	public void setWidth(String width) {
		this.width = width;
	}
	
	/**
	 * @return height
	 */
	public String getHeight() {
		return height;
	}
	
	/**
	 * set the height to height
	 * @param height
	 */
	public void setHeight(String height) {
		this.height = height;
	}
	
}
